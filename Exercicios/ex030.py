numeroDigitado = int(input('Digite um numero para saber se é impar ou par: '))

""" Resto da divisão """
if numeroDigitado % 2 == 0:
    print('O numero digitado foi "{}" e é par.'.format(numeroDigitado))
else:
    print('O numero digitado foi "{}" e é impar.'.format(numeroDigitado))

