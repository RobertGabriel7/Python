l= input('Digite algo: ')

print("É um número: ",l.isnumeric())
print("É uma letra: ",l.isalpha())
print("É um número decimal: ",l.isdecimal())
print("É letra e número: ",l.isalnum())
print("Pode ser usado como um identificador de uma variavel ?",l.isidentifier())
print("Tem espaço em branco? ",l.isspace())
print("A primeira letra é : ",l.istitle())
print("É tudo minusculo?",l.islower())
print("É tudo maiusculo? ",l.isupper())

